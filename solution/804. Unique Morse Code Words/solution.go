func uniqueMorseRepresentations(words []string) (result int) {
    morse_code_map := make(map[string]int)
    mapping := map[string]string{
        "a": ".-",
        "b": "-...",
        "c": "-.-.",
        "d": "-..",
        "e": ".",
        "f": "..-.",
        "g": "--.",
        "h": "....",
        "i": "..",
        "j": ".---",
        "k": "-.-",
        "l": ".-..",
        "m": "--",
        "n": "-.",
        "o": "---",
        "p": ".--.",
        "q": "--.-",
        "r": ".-.",
        "s": "...",
        "t": "-",
        "u": "..-",
        "v": "...-",
        "w": ".--",
        "x": "-..-",
        "y": "-.--",
        "z": "--..",
    }
    for _, value := range words {
        word := ""
        for _, letter := range strings.Split(value, "") {
            word += mapping[letter]
        }
        morse_code_map[word]++
    }
    result = len(morse_code_map)
    return
}

